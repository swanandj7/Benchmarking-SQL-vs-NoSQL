        String pwd="arijit";
        Connection con=DriverManager.getConnection(myUrl,"root",pwd);
        Statement st=con.createStatement();
        String insertQuery="INSERT INTO insert_tab values(?,?,?)";
        String field1=jTextField1.getText();
        String field2=jTextField2.getText();
        String field3=jTextField3.getText();
